class OddEven{
	public static void main(String[] args){
		int n=50;
		System.out.println("Display Even numbers");
		for(int i=0; i<=n; i++){
			if(i%2==0){
				System.out.println(i);
				
		}
		{
		System.out.println("Display ODD Numbers:");
		int x=50;
		for(i=0;i<=x;i++){
			if(i%2!=0){
				System.out.println(i);
			}
		}
		}
	}
}
}